"""Generate weekly distribution-center labor cost records.

Schema fields:
    labor_id
    dc_id
    week_start_date
    department
    role
    labor_type
    hours_worked
    cost_per_hour
    total_cost
    headcount

Business-driven realism notes:
- Records are generated by DC, department, and week.
- Department workload scales with DC capacity.
- Overtime labor uses a 1.5x hourly premium.
"""

import logging

import numpy as np
import pandas as pd

import data_generator.config as config

LABOR_COLUMNS = [
    "labor_id",
    "dc_id",
    "week_start_date",
    "department",
    "role",
    "labor_type",
    "hours_worked",
    "cost_per_hour",
    "total_cost",
    "headcount",
]

DEPARTMENT_BASE_HOURS = {
    "Receiving": 120,
    "Picking": 200,
    "Packing": 160,
    "Shipping": 140,
    "Inventory Control": 80,
}

DEPARTMENT_ROLE = {
    "Receiving": "Receiving Associate",
    "Picking": "Picker",
    "Packing": "Packer",
    "Shipping": "Shipping Clerk",
    "Inventory Control": "Inventory Analyst",
}

DEPARTMENT_RATE_RANGE = {
    "Receiving": (19.0, 23.0),
    "Picking": (18.0, 22.0),
    "Packing": (18.0, 21.0),
    "Shipping": (20.0, 24.0),
    "Inventory Control": (22.0, 27.0),
}


def generate_labor_costs(dcs_df):
    """Generate weekly labor cost records for each DC and department.

    Args:
        dcs_df (pd.DataFrame): Distribution centers including capacity fields.

    Returns:
        pd.DataFrame: Labor costs in canonical column order.
    """
    logger = logging.getLogger(__name__)
    logger.info("Generating labor_costs table")

    if dcs_df is None or dcs_df.empty:
        logger.warning("No distribution centers provided; returning empty labor costs")
        return pd.DataFrame(columns=LABOR_COLUMNS)

    start = pd.Timestamp(config.START_DATE).normalize()
    end = pd.Timestamp(config.END_DATE).normalize()
    start_monday = start - pd.to_timedelta(start.weekday(), unit="D")
    week_starts = pd.date_range(start=start_monday, end=end, freq="W-MON")

    if len(week_starts) == 0:
        return pd.DataFrame(columns=LABOR_COLUMNS)

    dc_ids = dcs_df["dc_id"].to_numpy(dtype=int)
    capacity = (
        pd.to_numeric(dcs_df.get("capacity_units", 50000), errors="coerce").fillna(50000).to_numpy()
    )
    capacity_scale = np.clip(capacity / 50000.0, 0.2, None)

    departments = np.array(list(DEPARTMENT_BASE_HOURS.keys()))
    n_dc = len(dc_ids)
    n_dept = len(departments)
    n_weeks = len(week_starts)
    n_rows = n_dc * n_dept * n_weeks

    dc_rep = np.repeat(dc_ids, n_dept * n_weeks)
    cap_rep = np.repeat(capacity_scale, n_dept * n_weeks)
    dept_rep = np.tile(np.repeat(departments, n_weeks), n_dc)
    week_rep = np.tile(np.tile(week_starts, n_dept), n_dc)

    base_hours = np.array([DEPARTMENT_BASE_HOURS[d] for d in dept_rep], dtype=float)
    variance = np.random.uniform(-0.15, 0.15, size=n_rows)
    hours_worked = base_hours * cap_rep * (1.0 + variance)
    hours_worked = np.round(np.clip(hours_worked, 1.0, None), 2)

    labor_type = np.random.choice(np.array(["Regular", "Overtime"]), size=n_rows, p=[0.90, 0.10])

    rate_low = np.array([DEPARTMENT_RATE_RANGE[d][0] for d in dept_rep], dtype=float)
    rate_high = np.array([DEPARTMENT_RATE_RANGE[d][1] for d in dept_rep], dtype=float)
    cost_per_hour = np.random.uniform(rate_low, rate_high)
    overtime_mask = labor_type == "Overtime"
    cost_per_hour[overtime_mask] = cost_per_hour[overtime_mask] * 1.5
    cost_per_hour = np.round(cost_per_hour, 2)

    total_cost = np.round(hours_worked * cost_per_hour, 2)
    headcount = np.maximum(1, np.floor(hours_worked / 40.0).astype(int))

    week_start_dates = pd.to_datetime(week_rep).date

    labor_df = pd.DataFrame(
        {
            "labor_id": np.arange(1, n_rows + 1),
            "dc_id": dc_rep,
            "week_start_date": week_start_dates,
            "department": dept_rep,
            "role": [DEPARTMENT_ROLE[d] for d in dept_rep],
            "labor_type": labor_type,
            "hours_worked": hours_worked,
            "cost_per_hour": cost_per_hour,
            "total_cost": total_cost,
            "headcount": headcount,
        }
    )

    logger.info("Generated %d labor_costs rows", len(labor_df))
    return labor_df[LABOR_COLUMNS]
